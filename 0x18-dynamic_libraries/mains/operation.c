/**
 * add->add two numbers a and b
 * Return: sum of a and b
 */

int add(int a, int b)
{
   return(a + b);
}
/**
 * sub->substruct  two numbers a and b
 * Return: reuslt of substraction a and b
 */
int sub(int a, int b)
{
   return(a - b);
}
/**
 * mul->multiply two numbers a and b
 * Return: product of a and b
 */
int mul(int a, int b)
{
   return(a * b);
}
/**
 * div->divide two numbers a and b
 * Return: quotient of a and b
 */
int div(int a, int b)
{
   return(a / b);
}
/**
 * mod->caluculate the remainder  of two numbers a and b
 * Return: modulo of a and b
 */
int mod(int a, int b)
{
   return(a % b);
}
